///了解字母對應的值
#include <stdio.h>
int main()
{
    printf("A的值:%d 數字65對應一個值: %c\n",'A',65);
    printf("B的值:%d 數字66對應一個值: %c\n",'B',66);
    printf("C的值:%d 數字67對應一個值: %c\n",'C',67);
    printf("Z的值:%d 數字90對應一個值: %c\n",'Z',90);
    printf("\n");
    printf("a的值:%d  數字97對應一個值:  %c\n",'a',97);
    printf("b的值:%d  數字98對應一個值:  %c\n",'b',98);
    printf("c的值:%d  數字99對應一個值:  %c\n",'c',99);
    printf("z的值:%d 數字122對應一個值: %c\n",'z',122);
}
